    private void jCboProdutoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jCboProdutoFocusLost
Produto produto = (Produto) jCboProduto.getSelectedItem();

double valorUni = produto.getValor();
jFntValor.setText(String.valueOf(valorUni));
double quantidade = Double.valueOf(jFntValor.getText());
jFntTotal.setText(String.valueOf(quantidade * valorUni));

        // TODO add your handling code here:
    }//GEN-LAST:event_jCboProdutoFocusLost

