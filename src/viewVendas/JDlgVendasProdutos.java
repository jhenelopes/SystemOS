    private void jBtnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOkActionPerformed
        vendasProdutos = new VendasProdutos();
       vendasProdutos.setIdvendasProdutos(jDlgVendas.vendasProdutosControle.getRowCount()+1);
        
       
        vendasProdutos.setQuantidade(Util.strInt(jTxtQuantidade.getText()));
         vendasProdutos.setProduto((Produto)jCboProduto.getSelectedItem());
         
        vendasProdutos.setValorunitario(Util.strDouble(jFntValor.getText()));        
        vendasProdutos.setVendas(jDlgVendas.viewBean());

               
        jDlgVendas.vendasProdutosControle.addList(vendasProdutos);
        setVisible(false); // TODO add your handling code here:
    }//GEN-LAST:event_jBtnOkActionPerformed

